// Figura 2.4: Welcome3.java
// Imprimindo múltiplas linhas de texto com uma única instrução.

public class Welcome3{
    public static void main( String[] args ){
        System.out.println( "Welcome\n to \n Java\n Programming!" );   // nova linha
        System.out.println( "\"Até parece\"");   // coloca " na string
        System.out.println( "\\Até parece\\");   // coloca \ na string
        System.out.println( "\tAté parece\t");   // da um tab na string
        System.out.println( "Até\r parece\rGata");   // joga a próxima palavra no inicio da linha
        }
}
